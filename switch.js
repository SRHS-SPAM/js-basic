let num = 2;

switch (num) {
  // 값을 확인할 변수
  case 1:
    // num === 1이면
    console.log("one");
    break;
  case 2:
    // num === 2이면
    console.log("two");
    break;
  case 3:
    // num === 3이면
    console.log("three");
    break;
  default:
    // 아무것도 아니면 like else
    console.log("default");
    break;
}
// case안에는 조건을 넣을 수는 없고, 값만 넣을 수 있음.
